    private void btn_add_prescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_prescriptionActionPerformed
        
                if (evt.getSource() == btn_add_prescription) {
                    String medicament = (String) list_medicament.getSelectedItem();
            if (txt_quantite.getText().equals("") || txt_nombre_de_jours.getText().equals("") || medicament.equals("")) {
                JOptionPane.showMessageDialog(this, "Les champs ne doivent pas être vide", "Info", JOptionPane.INFORMATION_MESSAGE, null);
            } else {
                String quantite = txt_quantite.getText();
                String jours = txt_nombre_de_jours.getText();
                int numConsultation = services.lastConsultation();
                
                int status = services.addPrescription(numConsultation,medicament,quantite,jours);
                if (status == 1) {
                    txt_quantite.setText("");
                    txt_nombre_de_jours.setText("");
                    JOptionPane.showMessageDialog(this, "Médicament " + medicament + " a été ajouté avec succés", "Info", JOptionPane.INFORMATION_MESSAGE, null);
                    TablePrescrit(numConsultation);
                } else {
                    JOptionPane.showMessageDialog(this, "Le médicament " + medicament + " n'a pas pu être ajouté", "Info", JOptionPane.INFORMATION_MESSAGE, null);
                }
            }
        }
        
        
        
    }//GEN-LAST:event_btn_add_prescriptionActionPerformed

