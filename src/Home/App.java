    private void btn_add_consultationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_consultationActionPerformed
        if (evt.getSource() == btn_add_consultation) {
            String patient = (String) list_patient.getSelectedItem();
            String medecin = (String) list_medecin.getSelectedItem();
            if (patient.equals("") || medecin.equals("")) {
                JOptionPane.showMessageDialog(this, "Les champs ne doivent pas être vide", "Info", JOptionPane.INFORMATION_MESSAGE, null);
            } else {
                int status = services.addConsultation(medecin, patient, date);
                if (status == 1) {
                    JOptionPane.showMessageDialog(this, "La consultation du patient " + patient + " a été ajouté avec succés", "Info", JOptionPane.INFORMATION_MESSAGE, null);
                    panelPrescription();
                    txt_value_medecin.setText(medecin);
                    txt_value_patient.setText(patient);
                   
                } else {
                    JOptionPane.showMessageDialog(this, "La consultation du patient " + patient + " n'a pas pu être ajouté", "Info", JOptionPane.INFORMATION_MESSAGE, null);
                }
            }
        }
    }//GEN-LAST:event_btn_add_consultationActionPerformed

